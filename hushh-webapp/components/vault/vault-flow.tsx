"use client";

import { useState, useEffect, useCallback, useRef } from "react";
import { Button, Card, CardContent } from "@/lib/morphy-ux/morphy";
import {
  Lock,
  Loader2,
  AlertCircle,
  Key,
  Check,
  Copy,
  Download,
  Shield,
  ArrowRight,
  Fingerprint,
} from "lucide-react";
import { VaultService } from "@/lib/services/vault-service";
import { downloadTextFile } from "@/lib/utils/native-download";
import { Input } from "@/components/ui/input";
import { Label } from "@/components/ui/label";
import { Alert, AlertDescription } from "@/components/ui/alert";
import {
  Dialog,
  DialogContent,
  DialogDescription,
  DialogFooter,
  DialogHeader,
  DialogTitle,
} from "@/components/ui/dialog";
import { morphyToast as toast } from "@/lib/morphy-ux/morphy";
import { User } from "firebase/auth";

import { useVault } from "@/lib/vault/vault-context";
import { HushhLoader } from "@/components/app-ui/hushh-loader";
import { Icon } from "@/lib/morphy-ux/ui";
import type { GeneratedVaultKeyMode } from "@/lib/services/vault-bootstrap-service";
import { VaultMethodService, type VaultMethod } from "@/lib/services/vault-method-service";

type VaultStep =
  | "checking"
  | "intro"
  | "create"
  | "unlock"
  | "recovery"
  | "method"
  | "success";
type VaultMode = "passphrase" | GeneratedVaultKeyMode;

interface VaultFlowProps {
  user: User;
  onSuccess: (meta?: { mode: VaultMode }) => void;
  // Callback to inform parent about current step (e.g. to hide headers)
  onStepChange?: (step: VaultStep) => void;
  enableGeneratedDefault?: boolean;
}

export function VaultFlow({
  user,
  onSuccess,
  onStepChange,
  enableGeneratedDefault = false,
}: VaultFlowProps) {
  const [step, setStep] = useState<VaultStep>("checking");
  const [error, setError] = useState<string | null>(null);
  const [passphrase, setPassphrase] = useState("");
  const [confirmPassphrase, setConfirmPassphrase] = useState("");
  const [recoveryKey, setRecoveryKey] = useState<string>("");
  const [recoveryKeyInput, setRecoveryKeyInput] = useState("");
  const [copied, setCopied] = useState(false);
  const [isUnlocking, setIsUnlocking] = useState(false);
  const [vaultMode, setVaultMode] = useState<VaultMode>("passphrase");
  const [availableGeneratedMethod, setAvailableGeneratedMethod] =
    useState<GeneratedVaultKeyMode | null>(null);
  const [unlockWithPassphraseFallback, setUnlockWithPassphraseFallback] =
    useState(false);
  const [pendingUnlockKey, setPendingUnlockKey] = useState<string | null>(null);
  const [recommendedQuickMethod, setRecommendedQuickMethod] =
    useState<VaultMethod | null>(null);
  const autoGeneratedPromptedRef = useRef(false);

  const { unlockVault } = useVault();

  // Notify parent of step changes
  useEffect(() => {
    onStepChange?.(step);
  }, [step, onStepChange]);

  const isGeneratedVaultMode =
    vaultMode === "generated_default_native_biometric" ||
    vaultMode === "generated_default_web_prf" ||
    vaultMode === "generated_default_native_passkey_prf";
  const hasActiveGeneratedWrapper =
    isGeneratedVaultMode && availableGeneratedMethod === vaultMode;

  const generatedUnlockLabel =
    vaultMode === "generated_default_web_prf" ||
    vaultMode === "generated_default_native_passkey_prf"
      ? "Passkey"
      : vaultMode === "generated_default_native_biometric"
        ? "Device security"
        : "Secure unlock";

  const finalizeUnlock = useCallback(async (decryptedKey: string): Promise<boolean> => {
    try {
      const { token, expiresAt } = await VaultService.getOrIssueVaultOwnerToken(user.uid);
      VaultService.setVaultCheckCache(user.uid, true);
      unlockVault(decryptedKey, token, expiresAt);
      setStep("success");
      setTimeout(() => onSuccess({ mode: vaultMode }), 1000);
      return true;
    } catch (tokenError) {
      console.error("Failed to issue VAULT_OWNER token:", tokenError);
      toast.error("Vault unlocked but failed to issue access token. Please try again.");
      return false;
    }
  }, [onSuccess, unlockVault, user.uid, vaultMode]);

  // Initial Vault Status Check
  useEffect(() => {
    const checkStatus = async () => {
      try {
        const hasVault = await VaultService.checkVault(user.uid);
        if (!hasVault) {
          setVaultMode("passphrase");
          setUnlockWithPassphraseFallback(false);
          setStep("intro");
          return;
        }

        try {
          const vaultData = await VaultService.getVaultState(user.uid);
          const primaryWrapper = VaultService.getPrimaryWrapper(vaultData);
          const quickMethod =
            (primaryWrapper.method === "generated_default_native_biometric" ||
              primaryWrapper.method === "generated_default_web_prf" ||
              primaryWrapper.method === "generated_default_native_passkey_prf")
              ? primaryWrapper
              : vaultData.wrappers.find(
                  (wrapper) =>
                    wrapper.method === "generated_default_native_biometric" ||
                    wrapper.method === "generated_default_web_prf" ||
                    wrapper.method === "generated_default_native_passkey_prf"
                );
          const nextQuickMethod =
            (quickMethod?.method as GeneratedVaultKeyMode | undefined) ?? null;
          setAvailableGeneratedMethod(nextQuickMethod);
          if (
            (vaultData.primaryMethod === "generated_default_native_biometric" ||
              vaultData.primaryMethod === "generated_default_web_prf" ||
              vaultData.primaryMethod === "generated_default_native_passkey_prf") &&
            !nextQuickMethod
          ) {
            setVaultMode("passphrase");
            setUnlockWithPassphraseFallback(true);
          } else {
            setVaultMode(vaultData.primaryMethod);
          }
        } catch (metadataError) {
          console.warn("Vault mode detection failed, defaulting to passphrase:", metadataError);
          setVaultMode("passphrase");
          setAvailableGeneratedMethod(null);
        }

        setUnlockWithPassphraseFallback(false);
        setStep("unlock");
      } catch (err) {
        console.error("Vault status check failed:", err);
        setError("Failed to check vault status. Please retry.");
      }
    };
    checkStatus();
  }, [user.uid]);

  const handleCreatePassphrase = async () => {
    if (passphrase.length < 8) {
      toast.error("Passphrase must be at least 8 characters");
      return;
    }
    if (passphrase !== confirmPassphrase) {
      toast.error("Passphrases do not match");
      return;
    }

    setIsUnlocking(true);
    try {
      setError(null);
      // 1. Generate encrypted vault data
      const vaultData = await VaultService.createVault(passphrase);
      const vaultKeyHash = await VaultService.hashVaultKey(vaultData.vaultKeyHex);

      // 2. Save multi-wrapper vault state (mandatory passphrase wrapper)
      await VaultService.setupVaultState(user.uid, {
        vaultKeyHash,
        primaryMethod: "passphrase",
        recoveryEncryptedVaultKey: vaultData.recoveryEncryptedVaultKey,
        recoverySalt: vaultData.recoverySalt,
        recoveryIv: vaultData.recoveryIv,
        wrappers: [
          {
            method: "passphrase",
            encryptedVaultKey: vaultData.encryptedVaultKey,
            salt: vaultData.salt,
            iv: vaultData.iv,
          },
        ],
      });

      // 3. Verify state persisted correctly before moving ahead.
      const persistedState = await VaultService.getVaultState(user.uid);
      await VaultService.assertVaultKeyMatchesState(
        persistedState,
        vaultData.vaultKeyHex
      );
      if (!VaultService.getWrapperByMethod(persistedState, "passphrase")) {
        throw new Error("Vault setup verification failed: passphrase wrapper missing.");
      }

      setVaultMode("passphrase");
      VaultService.setVaultCheckCache(user.uid, true);
      setRecoveryKey(vaultData.recoveryKey);
      setStep("recovery"); // Show recovery key dialog
    } catch (err: any) {
      console.error("Create vault error:", err);
      toast.error(err.message || "Failed to create vault");
    } finally {
      setIsUnlocking(false);
    }
  };

  const handleUnlockPassphrase = useCallback(async () => {
    setIsUnlocking(true);
    try {
      setError(null);
      const vaultData = await VaultService.getVaultState(user.uid);
      const decryptedKey = await VaultService.unlockWithMethod({
        state: vaultData,
        method: "passphrase",
        secretMaterial: passphrase,
      });

      if (decryptedKey) {
        await finalizeUnlock(decryptedKey);
      } else {
        const message = "Invalid passphrase. Please try again.";
        setError(message);
        toast.error(message);
      }
    } catch (err: any) {
      console.error("Unlock error:", err);
      const message = "Failed to unlock vault. Please try again.";
      setError(message);
      toast.error(message);
    } finally {
      setIsUnlocking(false);
    }
  }, [finalizeUnlock, passphrase, user.uid]);

  const handleUnlockGeneratedDefault = useCallback(async () => {
    setIsUnlocking(true);
    try {
      setError(null);
      const vaultData = await VaultService.getVaultState(user.uid);
      const generatedWrapper = VaultService.getWrapperByMethod(vaultData, vaultMode, {
        wrapperId:
          vaultData.primaryMethod === vaultMode
            ? vaultData.primaryWrapperId
            : undefined,
      });
      if (!generatedWrapper) {
        throw new Error("Quick unlock wrapper not enrolled.");
      }
      const decryptedKey = await VaultService.unlockGeneratedDefaultVault({
        userId: user.uid,
        encryptedVaultKey: generatedWrapper.encryptedVaultKey,
        salt: generatedWrapper.salt,
        iv: generatedWrapper.iv,
        keyMode: generatedWrapper.method,
        authMethod: generatedWrapper.method,
                        passkeyCredentialId: generatedWrapper.passkeyCredentialId,
                        passkeyPrfSalt: generatedWrapper.passkeyPrfSalt,
                      });

      if (!decryptedKey) {
        throw new Error("Generated vault mode unavailable. Use passphrase.");
      }

      await VaultService.assertVaultKeyMatchesState(vaultData, decryptedKey);
      await finalizeUnlock(decryptedKey);
    } catch (err: any) {
      console.error("Generated vault unlock failed:", err);
      const message = err?.message || "Failed to unlock with secure default key.";
      setError(message);
      toast.error(message);
    } finally {
      setIsUnlocking(false);
    }
  }, [finalizeUnlock, user.uid, vaultMode]);

  const handleRecoveryKeySubmit = async () => {
    setIsUnlocking(true);
    try {
      setError(null);
      const vaultData = await VaultService.getVaultState(user.uid);
      const decryptedKey = await VaultService.unlockVaultWithRecoveryKey(
        recoveryKeyInput,
        vaultData.recoveryEncryptedVaultKey,
        vaultData.recoverySalt,
        vaultData.recoveryIv
      );

      if (decryptedKey) {
        await VaultService.assertVaultKeyMatchesState(vaultData, decryptedKey);
        await finalizeUnlock(decryptedKey);
      } else {
        const message = "Invalid recovery key. Please try again.";
        setError(message);
        toast.error(message);
      }
    } catch (err: unknown) {
      console.error("Recovery key unlock failed:", err);
      const message = "Failed to unlock with recovery key. Please try again.";
      setError(message);
      toast.error(message);
    } finally {
      setIsUnlocking(false);
    }
  };

  const handleCopyRecoveryKey = () => {
    navigator.clipboard.writeText(recoveryKey);
    setCopied(true);
    toast.success("Recovery key copied to clipboard");
    setTimeout(() => setCopied(false), 2000);
  };

  const handleRecoveryKeyContinue = async () => {
    try {
      // Auto-unlock now that unique key is saved
      const vaultData = await VaultService.getVaultState(user.uid);
      const passphraseWrapper = VaultService.getWrapperByMethod(
        vaultData,
        "passphrase"
      );
      let decryptedKey: string | null = null;

      if (passphraseWrapper && passphrase) {
        decryptedKey = await VaultService.unlockWithMethod({
          state: vaultData,
          method: "passphrase",
          secretMaterial: passphrase,
        });
      }

      if (!decryptedKey) {
        throw new Error("Auto-unlock returned empty vault key.");
      }

      // Post-create optional method upsell: keep a single active KEK, but allow
      // immediate switch to quick unlock before entering the app.
      if (vaultMode === "passphrase" && enableGeneratedDefault) {
        const capability = await VaultMethodService.getCapabilityMatrix();
        if (capability.recommendedMethod !== "passphrase") {
          setPendingUnlockKey(decryptedKey);
          setRecommendedQuickMethod(capability.recommendedMethod);
          setStep("method");
          return;
        }
      }

      const finalized = await finalizeUnlock(decryptedKey);
      if (!finalized) return;
    } catch (err) {
      console.error("Auto-unlock after creation failed", err);
      // If auto-unlock fails, send user to unlock screen to try manually.
      toast.error(
        vaultMode === "passphrase"
          ? "Auto-unlock failed. Please enter your passphrase."
          : "Auto-unlock failed. Try secure unlock or recovery key."
      );
      setStep("unlock");
      return;
    }
  };

  useEffect(() => {
    if (
      step !== "unlock" ||
      !hasActiveGeneratedWrapper ||
      unlockWithPassphraseFallback
    ) {
      autoGeneratedPromptedRef.current = false;
      return;
    }
    if (autoGeneratedPromptedRef.current) return;
    autoGeneratedPromptedRef.current = true;
    void handleUnlockGeneratedDefault();
  }, [
    handleUnlockGeneratedDefault,
    hasActiveGeneratedWrapper,
    step,
    unlockWithPassphraseFallback,
  ]);

  if (step === "checking") {
    if (error) {
      return (
        <Card variant="none" effect="fill">
          <CardContent className="p-6 text-center py-8">
            <div className="space-y-4">
              <div className="text-destructive mb-2">
                <Icon icon={AlertCircle} size={32} className="mx-auto" />
              </div>
              <p className="text-muted-foreground">{error}</p>
              <Button
                onClick={() => window.location.reload()}
                variant="none"
                className="border border-input bg-background hover:bg-accent hover:text-accent-foreground"
              >
                Retry
              </Button>
            </div>
          </CardContent>
        </Card>
      );
    }

    return <HushhLoader label="Checking vault status..." />;
  }

  return (
    <>
      <Card variant="none" effect="fill">
        <CardContent className="p-6 space-y-4">
          {/* Intro / Education Step */}
          {step === "intro" && (
            <div className="space-y-6 text-center animate-in fade-in slide-in-from-bottom-4 duration-500">
              <div className="flex justify-center mb-4">
                <div className="h-20 w-20 rounded-2xl bg-primary/10 flex items-center justify-center">
                  <Icon icon={Shield} size={40} className="text-primary" />
                </div>
              </div>
              
              <div className="space-y-2">
                <h3 className="text-2xl font-bold tracking-tight">Secure Your Digital Vault</h3>
                <p className="text-muted-foreground text-balance max-w-sm mx-auto">
                  Hushh uses end-to-end encryption to protect your personal data.
                  Create a passphrase first, then optionally enable faster device unlock.
                </p>
              </div>

              <div className="text-left bg-muted/50 rounded-xl p-4 space-y-3 text-sm border border-border/50">
                <div className="flex gap-3">
                  <div className="mt-0.5 min-w-[1.25rem] text-primary">
                     <Icon icon={Check} size="md" />
                  </div>
                  <p><span className="font-semibold block text-foreground">You hold the only key</span> We cannot see your data or reset your password.</p>
                </div>
                <div className="flex gap-3">
                   <div className="mt-0.5 min-w-[1.25rem] text-primary">
                     <Icon icon={Check} size="md" />
                  </div>
                  <p><span className="font-semibold block text-foreground">Encrypted by default</span> There is no plaintext-at-rest path.</p>
                </div>
              </div>

              <div className="space-y-2">
                <Button 
                  variant="gradient" 
                  size="xl" 
                  fullWidth
                  className="group whitespace-normal text-center leading-snug px-4 text-base sm:text-lg"
                  onClick={() => {
                    setError(null);
                    setStep("create");
                  }}
                >
                  I Understand, Create Vault
                  <Icon
                    icon={ArrowRight}
                    size="md"
                    className="ml-2 transition-transform group-hover:translate-x-1"
                  />
                </Button>

              </div>
            </div>
          )}

          {/* Create Passphrase */}
          {step === "create" && (
            <div className="space-y-4">
              <div className="text-center">
                <Icon icon={Lock} size={48} className="mx-auto text-primary mb-4" />
                <h3 className="font-semibold text-xl">Create Your Vault Passphrase</h3>
                <p className="text-base text-muted-foreground mt-2">
                  This passphrase encrypts your data. We never see it.
                </p>
              </div>
              <div className="space-y-3">
                <Label htmlFor="passphrase" className="text-base">Passphrase</Label>
                <Input
                  id="passphrase"
                  type="password"
                  placeholder="Enter a strong passphrase"
                  value={passphrase}
                  onChange={(e) => setPassphrase(e.target.value)}
                  autoFocus
                  className="h-14 text-lg px-4"
                />
              </div>
              <div className="space-y-3">
                <Label htmlFor="confirm" className="text-base">Confirm Passphrase</Label>
                <Input
                  id="confirm"
                  type="password"
                  placeholder="Re-enter passphrase"
                  value={confirmPassphrase}
                  onChange={(e) => setConfirmPassphrase(e.target.value)}
                  className="h-14 text-lg px-4"
                />
              </div>
              <Button
                variant="gradient"
                effect="glass"
                size="xl"
                fullWidth
                className="mt-4"
                onClick={handleCreatePassphrase}
                disabled={isUnlocking || passphrase.length < 8 || passphrase !== confirmPassphrase}
              >
                {isUnlocking ? (
                  <>
                    <Icon icon={Loader2} size="md" className="mr-2 animate-spin" /> Creating...
                  </>
                ) : (
                  "Create Vault"
                )}
              </Button>
            </div>
          )}

          {/* Unlock Passphrase */}
          {step === "unlock" && (
            <div className="space-y-4">
              <div className="text-center">
                <Icon
                  icon={isGeneratedVaultMode ? Fingerprint : Lock}
                  size={48}
                  className="mx-auto text-primary mb-4"
                />
                <h3 className="font-semibold text-xl">Unlock Your Vault</h3>
                <p className="text-base text-muted-foreground mt-2">
                  {isGeneratedVaultMode
                    ? "Use your device security to decrypt your vault key"
                    : "Enter your passphrase to decrypt your data"}
                </p>
              </div>
              {(!isGeneratedVaultMode || unlockWithPassphraseFallback) && (
                <div className="space-y-3">
                  <Label htmlFor="unlock-passphrase" className="text-base">Passphrase</Label>
                  <Input
                    id="unlock-passphrase"
                    type="password"
                    placeholder="Enter your passphrase"
                    value={passphrase}
                    onChange={(e) => setPassphrase(e.target.value)}
                    onKeyDown={(e) =>
                      e.key === "Enter" && handleUnlockPassphrase()
                    }
                    autoFocus
                    className="h-14 text-lg px-4"
                  />
                </div>
              )}
              {error && (
                <p className="text-sm text-destructive">{error}</p>
              )}
              <div className="flex flex-col gap-3 pt-2">
                {(!hasActiveGeneratedWrapper || unlockWithPassphraseFallback) && (
                  <Button
                    variant="gradient"
                    effect="glass"
                    size="xl"
                    fullWidth
                    onClick={() => void handleUnlockPassphrase()}
                    disabled={isUnlocking || !passphrase}
                  >
                    {isUnlocking ? (
                      <>
                        <Icon icon={Loader2} size="md" className="mr-2 animate-spin" /> Unlocking...
                      </>
                    ) : unlockWithPassphraseFallback ? (
                      "Unlock with passphrase"
                    ) : (
                      "Unlock with passphrase"
                    )}
                  </Button>
                )}

                {hasActiveGeneratedWrapper && !unlockWithPassphraseFallback && (
                  <div className="rounded-xl border border-border/60 bg-muted/20 px-4 py-3 text-sm text-muted-foreground">
                    {isUnlocking ? (
                      <span className="inline-flex items-center">
                        <Icon icon={Loader2} size="md" className="mr-2 animate-spin" />
                        Prompting {generatedUnlockLabel.toLowerCase()}...
                      </span>
                    ) : (
                      `Prompting ${generatedUnlockLabel.toLowerCase()}...`
                    )}
                  </div>
                )}

                {hasActiveGeneratedWrapper && !unlockWithPassphraseFallback && error && (
                  <Button
                    variant="none"
                    effect="fade"
                    size="xl"
                    fullWidth
                    onClick={() => void handleUnlockGeneratedDefault()}
                    disabled={isUnlocking}
                  >
                    Try {generatedUnlockLabel.toLowerCase()} again
                  </Button>
                )}
                {hasActiveGeneratedWrapper && !unlockWithPassphraseFallback && (
                  <Button
                    variant="none"
                    effect="fade"
                    size="xl"
                    fullWidth
                    onClick={() => {
                      setError(null);
                      setUnlockWithPassphraseFallback(true);
                    }}
                    disabled={isUnlocking}
                  >
                    Use passphrase fallback
                  </Button>
                )}
                {hasActiveGeneratedWrapper && unlockWithPassphraseFallback && (
                  <Button
                    variant="none"
                    effect="fade"
                    size="xl"
                    fullWidth
                    onClick={() => {
                      setError(null);
                      setPassphrase("");
                      setUnlockWithPassphraseFallback(false);
                    }}
                    disabled={isUnlocking}
                  >
                    Use {generatedUnlockLabel.toLowerCase()}
                  </Button>
                )}
                {!hasActiveGeneratedWrapper &&
                  availableGeneratedMethod &&
                  !unlockWithPassphraseFallback && (
                    <Button
                      variant="none"
                      effect="fade"
                      size="xl"
                      fullWidth
                      onClick={() => {
                        setError(null);
                        setVaultMode(availableGeneratedMethod);
                      }}
                      disabled={isUnlocking}
                    >
                      Use{" "}
                      {availableGeneratedMethod === "generated_default_web_prf" ||
                      availableGeneratedMethod === "generated_default_native_passkey_prf"
                        ? "passkey"
                        : "device security"}
                    </Button>
                  )}
                <Button
                  variant="none"
                  effect="glass"
                  size="xl"
                  fullWidth
                  onClick={() => {
                    setError(null);
                    setStep("recovery");
                  }}
                  disabled={isUnlocking}
                >
                  Use Recovery Key
                </Button>
              </div>
            </div>
          )}

          {/* Recovery Key Input */}
          {step === "recovery" && !recoveryKey && (
            <div className="space-y-4">
              <div className="text-center">
                <Icon icon={Key} size={48} className="mx-auto text-primary mb-4" />
                <h3 className="font-semibold text-xl">Enter Recovery Key</h3>
                <p className="text-base text-muted-foreground mt-2">
                  Enter your recovery key to unlock your vault
                </p>
              </div>
              <div className="space-y-3">
                <Label htmlFor="recovery-key" className="text-base">Recovery Key</Label>
                <Input
                  id="recovery-key"
                  placeholder="HRK-XXXX-XXXX-XXXX-XXXX"
                  value={recoveryKeyInput}
                  onChange={(e) =>
                    setRecoveryKeyInput(e.target.value.toUpperCase())
                  }
                  className="h-14 text-lg px-4 font-mono"
                />
                {error && (
                  <p className="text-sm text-destructive">{error}</p>
                )}
              </div>
              <div className="flex flex-col gap-3 pt-2">
                <Button
                  variant="gradient"
                  effect="glass"
                  size="xl"
                  fullWidth
                  className="text-lg font-semibold"
                  onClick={handleRecoveryKeySubmit}
                  disabled={isUnlocking || !recoveryKeyInput}
                >
                  {isUnlocking ? (
                    <>
                      <Icon icon={Loader2} size="md" className="mr-2 animate-spin" /> Unlocking...
                    </>
                  ) : (
                    "Unlock"
                  )}
                </Button>
                <Button
                  variant="none"
                  effect="glass"
                  size="xl"
                  fullWidth
                  className="text-base"
                  onClick={() => {
                    setError(null);
                    setStep("unlock");
                  }}
                  disabled={isUnlocking}
                >
                  {hasActiveGeneratedWrapper ? generatedUnlockLabel : "Use Passphrase"}
                </Button>
              </div>
            </div>
          )}

          {/* Success */}
          {step === "success" && (
            <div className="text-center py-4">
              <div className="text-4xl mb-4">✅</div>
              <p className="text-muted-foreground">
                Vault unlocked, redirecting...
              </p>
            </div>
          )}

          {step === "method" && (
            <div className="space-y-4">
              <div className="text-center">
                <Icon
                  icon={
                    recommendedQuickMethod === "generated_default_web_prf" ||
                    recommendedQuickMethod === "generated_default_native_passkey_prf"
                      ? Key
                      : Fingerprint
                  }
                  size={48}
                  className="mx-auto text-primary mb-4"
                />
                <h3 className="font-semibold text-xl">Enable quicker unlock?</h3>
                <p className="text-base text-muted-foreground mt-2">
                  You can keep passphrase unlock, or enable{" "}
                  {recommendedQuickMethod === "generated_default_web_prf" ||
                  recommendedQuickMethod === "generated_default_native_passkey_prf"
                    ? "passkey"
                    : "device biometric"}{" "}
                  and still retain recovery-key fallback.
                </p>
              </div>

              <div className="flex flex-col gap-3 pt-2">
                <Button
                  variant="gradient"
                  effect="glass"
                  size="xl"
                  fullWidth
                  disabled={isUnlocking || !pendingUnlockKey || !recommendedQuickMethod}
                  onClick={async () => {
                    if (!pendingUnlockKey || !recommendedQuickMethod) return;
                    setIsUnlocking(true);
                    try {
                      const result = await VaultMethodService.switchMethod({
                        userId: user.uid,
                        currentVaultKey: pendingUnlockKey,
                        displayName: user.displayName || user.email || "Hushh User",
                        targetMethod: recommendedQuickMethod,
                      });
                      setVaultMode(result.method);
                      const finalized = await finalizeUnlock(pendingUnlockKey);
                      if (!finalized) return;
                      toast.success(
                        result.method === "generated_default_web_prf" ||
                        result.method === "generated_default_native_passkey_prf"
                          ? "Passkey unlock enabled."
                          : "Biometric unlock enabled."
                      );
                    } catch (err: any) {
                      console.error("Quick unlock enable failed:", err);
                      toast.error(
                        err?.message || "Couldn't enable quick unlock right now."
                      );
                    } finally {
                      setIsUnlocking(false);
                    }
                  }}
                >
                  {isUnlocking ? (
                    <>
                      <Icon icon={Loader2} size="md" className="mr-2 animate-spin" />
                      Enabling...
                    </>
                  ) : (
                    `Enable ${
                      recommendedQuickMethod === "generated_default_web_prf" ||
                      recommendedQuickMethod === "generated_default_native_passkey_prf"
                        ? "Passkey"
                        : "Biometric"
                    }`
                  )}
                </Button>

                <Button
                  variant="none"
                  effect="fade"
                  size="xl"
                  fullWidth
                  className="whitespace-normal text-center leading-snug px-4 text-sm sm:text-base"
                  disabled={isUnlocking || !pendingUnlockKey}
                  onClick={async () => {
                    if (!pendingUnlockKey) return;
                    await finalizeUnlock(pendingUnlockKey);
                  }}
                >
                  Not now, continue with passphrase
                </Button>
              </div>
            </div>
          )}
        </CardContent>
      </Card>

      {/* Recovery Key Dialog (New User) */}
      <Dialog
        open={step === "recovery" && !!recoveryKey}
        onOpenChange={() => {}}
      >
        <DialogContent
          className="sm:max-w-md"
          onPointerDownOutside={(e) => e.preventDefault()}
        >
          <DialogHeader>
            <div className="flex items-center gap-2">
              <Icon icon={Key} size="lg" className="text-orange-500" />
              <DialogTitle>Save Your Recovery Key</DialogTitle>
            </div>
            <DialogDescription>
              This is the ONLY way to recover your vault if you forget your
              vault credentials. Store it somewhere safe!
            </DialogDescription>
          </DialogHeader>

          <div className="space-y-4">
            <Alert className="bg-orange-500/10 border-orange-500/50">
              <Icon icon={AlertCircle} size="sm" className="text-orange-500" />
              <AlertDescription className="text-orange-700 dark:text-orange-300">
                Write this down or save it securely. You cannot recover it
                later!
              </AlertDescription>
            </Alert>

            <div className="p-4 bg-muted rounded-lg border-2 border-dashed">
              <code className="text-lg font-mono font-bold tracking-wide">
                {recoveryKey}
              </code>
            </div>

            <div className="flex flex-col gap-2 sm:flex-row">
              <Button
                variant="none"
                className="flex-1 border border-gray-200 dark:border-gray-700 whitespace-normal"
                onClick={handleCopyRecoveryKey}
              >
                {copied ? (
                  <>
                    <Icon icon={Check} size="sm" className="mr-2 text-green-500" />
                    Copied!
                  </>
                ) : (
                  <>
                    <Icon icon={Copy} size="sm" className="mr-2" />
                    Copy
                  </>
                )}
              </Button>
              <Button
                variant="none"
                className="flex-1 border border-gray-200 dark:border-gray-700 whitespace-normal"
                onClick={async () => {
                  const content = `Hushh Recovery Key\n\n${recoveryKey}\n\nStore this file securely. This is the ONLY way to recover your vault if you lose your vault credentials.`;
                  await downloadTextFile(content, "hushh-recovery-key.txt");
                }}
              >
                <Icon icon={Download} size="sm" className="mr-2" />
                Download
              </Button>
            </div>
          </div>

          <DialogFooter>
            <Button
              variant="gradient"
              effect="glass"
              className="w-full whitespace-normal text-center leading-snug h-auto min-h-12 px-4"
              onClick={handleRecoveryKeyContinue}
            >
              I've Saved My Recovery Key
            </Button>
          </DialogFooter>
        </DialogContent>
      </Dialog>
    </>
  );
}
